k = 0
k2 = 0
k23 = 0
# k = k2 = 0
# k, k2 = 0, 0
for i in range(10, 99+1):  # [ )
    print(i, end=' ')
    k += 1  # k = k + 1 k++
    if i % 2 == 0:
        k2 += 1
    if i % 3 == 0 and i % 2 == 0:
        k23 += 1
print()
print('Количество двухзначных чисел равно', k)
print('Количество двухзначных четных чисел равно', k2)
print('Количество двухзначных четных и кратных трем чисел равно', k23)